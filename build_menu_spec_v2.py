def hdr(ws, row, cols, widths=None):
    """헤더 행 그리기. cols = [(value, width_override), ...] or [value, ...]"""
    for i, c in enumerate(cols, 1):
        v = c[0] if isinstance(c, tuple) else c
        w = c[1] if isinstance(c, tuple) else None
        cell = ws.cell(row, i, v)
        cell.font = H_FONT; cell.fill = FI(BLUE)
        cell.alignment = CENTER; cell.border = BORDER
        if widths and i <= len(widths):
            ws.column_dimensions[get_column_letter(i)].width = widths[i-1]
        elif w:
            ws.column_dimensions[get_column_letter(i)].width = w
    ws.row_dimensions[row].height = 26


def row_w(ws, r, vals, bold_col=None, fill=None):
    for i, v in enumerate(vals, 1):
        c = ws.cell(r, i, v)
        c.border = BORDER; c.alignment = TOPL
        c.font = B_BOLD if bold_col and i == bold_col else B_FONT
        if fill: c.fill = FI(fill)
    ws.row_dimensions[r].height = 15


def sec_row(ws, r, label, ncols):
    ws.row_dimensions[r].height = 20
    for i in range(1, ncols+1):
        ws.cell(r, i).fill = FI(SEC_BG)
        ws.cell(r, i).border = BORDER
    ws.cell(r, 1, label).font = SEC
    ws.cell(r, 1).alignment = AL("left", "center", False)


